public class Demo2 {
    public boolean palindromeCheck(String str) {
        str = str.toLowerCase();
        int n = str.length();
        for (int i = 0; i < n / 2; i++) {
            if (str.charAt(i) != str.charAt(n - i - 1)) {
                return false;
            }
        }
        return true;
    }
}
